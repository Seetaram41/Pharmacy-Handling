    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String medicineId =txtMedicineId.getText();
        if(medicineId.equals("")){
            JOptionPane.showMessageDialog(null, "Medicine Id is Required to search Medicine");
        }
        else{
            try {
                Connection con = DbConnection.getCon();
                PreparedStatement ps = con.prepareStatement("select *from medicine where uniqueId = ?");
                ps.setString(1, medicineId);
                ResultSet rs =ps.executeQuery();
                while(rs.next()){
                    checkMedicine=1;
                    txtMedicineName.setText(rs.getString("name"));
                    txtCompanyName.setText(rs.getString("companyName"));
                    txtQuantity.setText(rs.getString("quantity"));
                    txtPrice.setText(rs.getString("price"));
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            if(checkMedicine==0){
                JOptionPane.showMessageDialog(null, "Medicine is not found Enter Correct Id");
            }
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

