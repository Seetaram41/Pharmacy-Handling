    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String medicineId =txtMedicineId.getText();
        String medicineName = txtMedicineName.getText();
        String company = txtCompanyName.getText();
        String quantity = txtQuantity.getText();
        String price = txtPrice.getText();
        if(company.equals("")){
            JOptionPane.showMessageDialog(null, "medicine company is required");
        }
        else if(medicineName.equals("")){
        JOptionPane.showMessageDialog(null, "medicine name is required");
        }
        else if(quantity.equals("")){
        JOptionPane.showMessageDialog(null, "medicine quantity is required");
        }
        else if(price.equals("")){
        JOptionPane.showMessageDialog(null, "medicine Price is required");
        }
        else if(!price.matches(numberPattern)){
        JOptionPane.showMessageDialog(null, "medicine Price is required");
        }
        else if(!quantity.matches(numberPattern)){
        JOptionPane.showMessageDialog(null, "medicine Price is required");
        }
        else{
            try {
                Connection con = DbConnection.getCon();
                PreparedStatement ps = con.prepareStatement("Update medicine set name = ?,companyName=?,quantity=?,price=? where uniqueId=?");
                ps.setString(1, medicineName);
                ps.setString(2, company);
                ps.setString(3, quantity);
                ps.setString(4, price);
                ps.setString(5, medicineId);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Medicine Details Updated succussfully");
                setVisible(false);
                new UpdateMedicine().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

